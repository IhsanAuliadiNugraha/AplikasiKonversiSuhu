     private void konversiSuhu() {
        try {
            double nilai = Double.parseDouble(txtSuhu.getText());
            String dari = cbDari.getSelectedItem().toString();
            String ke = cbKe.getSelectedItem().toString();
            double hasil = 0;

            // Konversi ke Celsius dulu
            switch (dari) {
                case "Fahrenheit": nilai = (nilai - 32) * 5 / 9; break;
                case "Reamur": nilai = nilai * 5 / 4; break;
                case "Kelvin": nilai = nilai - 273.15; break;
            }

            // Dari Celsius ke skala tujuan
            switch (ke) {
                case "Celsius": hasil = nilai; break;
                case "Fahrenheit": hasil = (nilai * 9 / 5) + 32; break;
                case "Reamur": hasil = nilai * 4 / 5; break;
                case "Kelvin": hasil = nilai + 273.15; break;
            }

            txtHasil.setText(String.format("%.2f Â°%s", hasil, ke.charAt(0)));
        } catch (NumberFormatException ex) {
            JOptionPane.showMessageDialog(this, "Masukkan angka yang valid!", "Error", JOptionPane.ERROR_MESSAGE);
        }
    }
